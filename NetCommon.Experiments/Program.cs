[SimpleJob(RunStrategy.Throughput, iterationCount: 2)]
[MinColumn, MaxColumn, MeanColumn, MedianColumn, MemoryDiagnoser]
public class BenchmarkTest
{
   [Benchmark]
   public string Run()
   {
      var builder = new CodeBuilder(
         stackalloc char[512],
         stackalloc char[16]);

      builder.NameSpace.EnableNullable();
      builder.NameSpace.AutoGeneratedComment();

      builder.NameSpace.Using("System.Collections");
      builder.NameSpace.Using("System.AA");
      builder.NameSpace.Using("System.BB");

      builder.Writer.WriteLine();
      builder.Writer.WriteLine("Wow");

      builder.Dispose();
      
      return string.Empty;
   }
   
   [Benchmark]
   public string Run2()
   {
      var stringWriter = new StringBuilder();
      
      stringWriter.AppendLine("#nullable enable");
      stringWriter.AppendLine();
      stringWriter.AppendLine("#nullable enable");
      stringWriter.AppendLine();
      stringWriter.AppendLine($"using {"System.Collections"}");
      stringWriter.AppendLine($"using {"System.AA"}");
      stringWriter.AppendLine($"using {"System.BB"}");
      
      return string.Empty;
   }
}